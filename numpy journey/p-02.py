#filled with 0
import numpy as np
arr=np.zeros((2,3))
print(arr)


#filled with 1
arr2=np.ones((2,3))
print(arr2)

#filled with any default values
arr3=np.full((2,3),4)
print(arr3)

#creating a sequence of number
arr4=np.arange(1,10,2)  #(start,stop,step)
print(arr4)

#identity matrices
arr5=np.eye(3)
print(arr5)

